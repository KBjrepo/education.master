package lesson5.model

class AddressData {

    public firstname
    public lastname
    public address1
    public postcode
    public city
    public phone
    public email

    AddressData(String firstname, lastname, address1, postcode, city, phone, email) {
        this.firstname = firstname
        this.lastname = lastname
        this.address1 = address1
        this.postcode = postcode
        this.city = city
        this.phone = phone
        this.email = email
    }

}
